# Operações Aritimeticas
# Dois operandos numéricos
x = 8.4
y = 2.3

print(x + y)
print(x - y)
print(x * y)
print(x / y)  # divisao normal
print(x ** y) # potencia
print(x // y) # divisao (inteira)
print(x % y)  # modulo (sobra da divisao)

print(round(x - y,2))

